// Helper functions
export function getFreezoneyEmirate(emirate) {
    return freezonesData.filter(fz => fz.emirate === emirate);
}

export function getFreezoneBySpecialization(specialization) {
    return freezonesData.filter(fz => fz.specialization.includes(specialization));
}

export function getFreezoneBudget(minBudget, maxBudget) {
    return freezonesData.filter(fz => fz.minCost >= minBudget && fz.minCost <= maxBudget);
}

export function getTopRatedFreezones(count = 10) {
    return [...freezonesData].sort((a, b) => b.rating - a.rating).slice(0, count);
}

export function searchFreezones(query) {
    const lowerQuery = query.toLowerCase();
    return freezonesData.filter(fz =>
        fz.name.toLowerCase().includes(lowerQuery) ||
        fz.fullName.toLowerCase().includes(lowerQuery) ||
        fz.specialization.toLowerCase().includes(lowerQuery) ||
        fz.popularActivities.some(act => act.toLowerCase().includes(lowerQuery))
    );
}
